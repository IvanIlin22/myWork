#include <unistd.h>

void		ft_putchar(char c)
{
	write(1, &c, 1);
}

int			main()
{
	int		i;
	char	str;
	
	i = 26;
	str = 'z';
	while (i)
	{
		if (!(i % 2))
			ft_putchar(str);
		else
			ft_putchar(str - 'a' + 'A');
		str--;
		i--;
	}
}