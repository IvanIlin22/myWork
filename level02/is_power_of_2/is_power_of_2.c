#include <stdio.h>

int	    is_power_of_2(unsigned int n)
{
	int	i;
	
	i = 1;
	while (n > 1)
	{
		if (n % 2)
			return (0);
		n /= 2;
	}
	return (1);		
}

int		main()
{
	int n = 128;
	printf("%i\n", is_power_of_2(n));
}
