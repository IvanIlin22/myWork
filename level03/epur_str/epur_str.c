#include <unistd.h>

int		main(int argc, char **argv)
{
	int	i;
	int	fl;
	
	i = 0;
	fl = 0;
	while (argv[1][i] == ' ' || argv[1][i] == '\t')
		i++;
	if (argc == 2)
	{
		while (argv[1][i])
		{
			if (argv[1][i] == ' ' || argv[1][i] == '\t')
				fl = 1;
			if (!(argv[1][i] == ' ' || argv[1][i] == '\t'))
				write(1, &argv[1][i], 1);
			if (fl && !(argv[1][i + 1] == ' ' || argv[1][i + 1] == '\t'))
			{
				write (1, " ", 1);
				fl = 0;
			}
			i++;
		}
	}
	write(1, "\n", 1);
}
