#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	*a = *a + *b;
	*b = *a - *b;
	*a = *a - *b;
}

int		main()
{
	int	a = 10;
	int b = 5;
	ft_swap(&a, &b);
	printf("a = %i\n", a);
	printf("b = %i", b);
}